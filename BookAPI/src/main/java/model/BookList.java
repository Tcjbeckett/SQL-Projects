package model;

public class BookList {

    private String bookBibKey;
    private String bookUrl;
    private String bookPages;
    private String bookTitle;
    private String bookByLine;
    private String bookPublisher;
    private String bookDescription;
    private String bookFormat;

    public String getBookBibKey() {
        return bookBibKey;
    }

    public void setBookBibKey(String bookBibKey) {
        this.bookBibKey = bookBibKey;
    }

    public String getBookUrl() {
        return bookUrl;
    }

    public void setBookUrl(String bookUrl) {
        this.bookUrl = bookUrl;
    }

    public String getBookPages() {
        return bookPages;
    }

    public void setBookPages(String bookPages) {
        this.bookPages = bookPages;
    }

    public String getBookTitle() {
        return bookTitle;
    }

    public void setBookTitle(String bookTitle) {
        this.bookTitle = bookTitle;
    }

    public String getBookByLine() {
        return bookByLine;
    }

    public void setBookByLine(String bookByLine) {
        this.bookByLine = bookByLine;
    }

    public String getBookPublisher() {
        return bookPublisher;
    }

    public void setBookPublisher(String bookPublisher) {
        this.bookPublisher = bookPublisher;
    }

    public String getBookDescription() {
        return bookDescription;
    }

    public void setBookDescription(String bookDescription) {
        this.bookDescription = bookDescription;
    }

    public String getBookFormat() {
        return bookFormat;
    }

    public void setBookFormat(String bookFormat) {
        this.bookFormat = bookFormat;
    }
}
